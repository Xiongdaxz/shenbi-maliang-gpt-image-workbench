function hasCjk(text: string) {
  return /[\u3400-\u9fff]/.test(text);
}

function preserveWhitespace(source: string, translated: string) {
  const leading = source.match(/^\s*/)?.[0] ?? "";
  const trailing = source.match(/\s*$/)?.[0] ?? "";
  return `${leading}${translated}${trailing}`;
}

function isKnownTranslatedValue(original: string, current: string) {
  const normalizedCurrent = current.trim();
  if (!normalizedCurrent) return false;
  return allConfigCopyLocales.some((locale) => translateConfigCopy(original.trim(), locale).trim() === normalizedCurrent);
}

export function translateConfigCopy(source: string, locale: LocaleCode) {
  if (!source || locale === "zh-CN") return source;
  if (locale === "zh-TW") return toTraditionalChinese(source);
  const trimmed = source.trim();
  if (locale === "en-US") {
    const exact = englishConfigCopy[trimmed];
    if (exact) return exact;
  } else {
    const localizedExact = localizedConfigCopy[locale as ConfigCopyLocale]?.[trimmed];
    if (localizedExact) return localizedExact;
  }
  let translated = trimmed;
  const phraseReplacements = locale === "en-US" ? englishPhraseReplacements : localizedPhraseReplacements[locale as ConfigCopyLocale] ?? [];
  for (const [from, to] of phraseReplacements) {
    translated = translated.split(from).join(to);
  }
  return translated === trimmed ? source : translated;
}

export function useConfigCopy() {
  const { resolvedLanguage } = useI18n();
  return useCallback((source: string) => translateConfigCopy(source, resolvedLanguage), [resolvedLanguage]);
}

function originalAttributeValue(element: Element, attribute: string, current: string, locale: LocaleCode) {
  let originals = attributeOriginals.get(element);
  if (!originals) {
    originals = new Map();
    attributeOriginals.set(element, originals);
  }
  const stored = originals.get(attribute);
  if (!stored) {
    originals.set(attribute, current);
    return current;
  }
  const translatedStored = translateConfigCopy(stored, locale);
  if (current !== translatedStored && hasCjk(current) && !isKnownTranslatedValue(stored, current)) {
    originals.set(attribute, current);
    return current;
  }
  return stored;
}

function translateTextNode(node: Text, locale: LocaleCode) {
  const parent = node.parentElement;
  if (!parent || parent.closest(SKIP_TEXT_SELECTOR)) return;
  const current = node.nodeValue ?? "";
  const trimmed = current.trim();
  if (!trimmed) return;
  const stored = textNodeOriginals.get(node);
  let original = stored ?? current;
  const translatedStored = preserveWhitespace(original, translateConfigCopy(original.trim(), locale));
  if (stored && current !== translatedStored && hasCjk(current) && !isKnownTranslatedValue(original, current)) {
    original = current;
    textNodeOriginals.set(node, original);
  } else if (!stored) {
    textNodeOriginals.set(node, original);
  }
  const translated = preserveWhitespace(original, translateConfigCopy(original.trim(), locale));
  if (node.nodeValue !== translated) node.nodeValue = translated;
}

function translateElementAttributes(root: HTMLElement, locale: LocaleCode) {
  const selector = TRANSLATABLE_ATTRIBUTES.map((attribute) => `[${attribute}]`).join(",");
  const elements = [root, ...Array.from(root.querySelectorAll(selector))];
  for (const element of elements) {
    if (element.closest(SKIP_ATTRIBUTE_SELECTOR)) continue;
    for (const attribute of TRANSLATABLE_ATTRIBUTES) {
      const current = element.getAttribute(attribute);
      if (!current?.trim()) continue;
      const original = originalAttributeValue(element, attribute, current, locale);
      const translated = translateConfigCopy(original.trim(), locale);
      if (translated !== current) element.setAttribute(attribute, translated);
    }
  }
}

function translateElementText(root: HTMLElement, locale: LocaleCode) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  let node = walker.nextNode();
  while (node) {
    translateTextNode(node as Text, locale);
    node = walker.nextNode();
  }
}

function translateConfigScope(root: HTMLElement, locale: LocaleCode) {
  translateElementText(root, locale);
  translateElementAttributes(root, locale);
}

export function useConfigCopyScope(rootRef: RefObject<HTMLElement | null>) {
  const { resolvedLanguage } = useI18n();
  const pendingFrameRef = useRef<number | null>(null);

  useEffect(() => {
    const root = rootRef.current;
    if (!root || typeof document === "undefined") return;

    const schedule = () => {
      if (pendingFrameRef.current !== null) window.cancelAnimationFrame(pendingFrameRef.current);
      pendingFrameRef.current = window.requestAnimationFrame(() => {
        pendingFrameRef.current = null;
        translateConfigScope(root, resolvedLanguage);
      });
    };

    schedule();
    const observer = new MutationObserver(schedule);
    observer.observe(root, {
      childList: true,
      subtree: true
    });

    return () => {
      observer.disconnect();
      if (pendingFrameRef.current !== null) {
        window.cancelAnimationFrame(pendingFrameRef.current);
        pendingFrameRef.current = null;
      }
    };
  }, [resolvedLanguage, rootRef]);
}
